# funkcja która zwraca sumę i testy do niej
def add(x, y):
    return x + y

# funkcja która zwraca iloczyn i testy do niej

def product(x, y):
    return x * y


# fukcja która odwraca napis

def words(string):
   return string[::-1]

def sq(n):
    return n**2



# fukcja która sprawdza czy slowo jest palindromem

def is_palindrom(word):
    return word == word[::-1]
